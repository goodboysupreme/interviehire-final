# Talent Finder — AI Candidate Sourcing / Web Talent Finder.
#
# A modular, COMPLIANT sourcing engine that plugs into the existing hiring
# pipeline. It discovers candidates only from permissioned sources (internal DB,
# uploaded CSV/resumes, ATS imports, recruiter-provided exports, approved APIs,
# and legally-accessible public pages), normalizes them, dedupes, scores fit with
# explainability + a bias check, and drafts recruiter-approved outreach.
#
# It NEVER scrapes login-walled / anti-bot / ToS-restricted platforms. LinkedIn,
# Internshala, Naukri, Indeed etc. exist only as DISABLED placeholder adapters
# that require official API access, written permission, or user-provided exports.
